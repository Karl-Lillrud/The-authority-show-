function initSvgIcons() {
  // Initialize all SVG icons
  document.querySelector(".history-icon").innerHTML = svgStore.historyIcon;
  document.querySelector(".credits-icon").innerHTML = svgStore.creditsIcon;

  // Initialize cart icon
  const cartIcon = document.querySelector(".cart-icon");
  if (cartIcon) {
    cartIcon.innerHTML = svgStore.cartIcon;
  }

  // Initialize close icon
  const closeIcon = document.querySelector(".close-icon");
  if (closeIcon) {
    closeIcon.innerHTML = svgStore.closeIcon;
  }

  // Credit pack icons
  document.querySelector(".credit-small-icon").innerHTML =
    svgStore.creditSmallIcon;
  document.querySelector(".credit-medium-icon").innerHTML =
    svgStore.creditMediumIcon;
  document.querySelector(".credit-large-icon").innerHTML =
    svgStore.creditLargeIcon;

  // Style icons
  document.querySelector(".style-minimal-icon").innerHTML =
    svgStore.styleMinimalIcon;
  document.querySelector(".style-premium-icon").innerHTML =
    svgStore.stylePremiumIcon;
  document.querySelector(".style-enterprise-icon").innerHTML =
    svgStore.styleEnterpriseIcon;

  // Subscription icons
  document.querySelector(".subscription-pro-icon").innerHTML =
    svgStore.subscriptionProIcon;
  document.querySelector(".subscription-team-icon").innerHTML =
    svgStore.subscriptionTeamIcon;
  document.querySelector(".subscription-enterprise-icon").innerHTML =
    svgStore.subscriptionEnterpriseIcon;

  // Add remove icons to any existing cart items
  const removeIcons = document.querySelectorAll(".remove-icon");
  removeIcons.forEach((icon) => {
    icon.innerHTML = svgStore.removeIcon;
  });
}
